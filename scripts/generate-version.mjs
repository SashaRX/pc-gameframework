import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const pkg = JSON.parse(readFileSync(join(__dirname, '../package.json'), 'utf8'));

const content = `// Auto-generated by scripts/generate-version.mjs — do not edit
export const FRAMEWORK_VERSION = '${pkg.version}';
`;

const outPath = join(__dirname, '../src/version.ts');
writeFileSync(outPath, content, 'utf8');
console.log(`Generated version.ts: ${pkg.version}`);