/**
 * Build worker code as inline string for embedding
 * Reads compiled worker .mjs and creates a string literal
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const workerPath = path.join(__dirname, '../build/esm-temp/workers/ktx-transcode.worker.js');
const outputPath = path.join(__dirname, '../build/esm-temp/loaders/worker-inline.js');

try {
  // Read worker code
  const workerCode = fs.readFileSync(workerPath, 'utf8');

  // Escape backticks and ${} for template literals
  const escapedCode = workerCode
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$');

  // Create module that exports the worker code as string
  const output = `/**
 * Inline worker code
 * Auto-generated by build-worker-inline.mjs
 */

export const WORKER_CODE = \`${escapedCode}\`;
`;

  // Write output
  fs.writeFileSync(outputPath, output, 'utf8');

  console.log('Worker inline code generated:', outputPath);

} catch (error) {
  console.error('Failed to build worker inline:', error);
  process.exit(1);
}
