/**
 * Build worker code as inline string for embedding.
 *
 * Strategy:
 * 1. Read compiled ktx-transcode.worker.js
 * 2. Escape it as a template literal
 * 3. Patch Ktx2ProgressiveLoader.js — replace the WORKER_CODE stub export with real code
 *    (TypeScript compiled the stub '' into the loader, so we patch the compiled output)
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const workerPath   = path.join(__dirname, '../build/esm-temp/workers/ktx-transcode.worker.js');
const loaderPath   = path.join(__dirname, '../build/esm-temp/loaders/Ktx2ProgressiveLoader.js');
const inlinePath   = path.join(__dirname, '../build/esm-temp/loaders/worker-inline.js');

// Read worker code
const workerCode = fs.readFileSync(workerPath, 'utf8');

// Escape for embedding in a template literal
const escaped = workerCode
  .replace(/\\/g, '\\\\')
  .replace(/`/g, '\\`')
  .replace(/\$/g, '\\$');

// ── 1. Update worker-inline.js (standalone module, used if imported separately) ──
const inlineModule = `/**
 * Inline worker code — auto-generated by build-worker-inline.mjs
 */
export const WORKER_CODE = \`${escaped}\`;
`;
fs.writeFileSync(inlinePath, inlineModule, 'utf8');
console.log('Worker inline module written:', inlinePath);

// ── 2. Patch Ktx2ProgressiveLoader.js ──
// TypeScript compiled `import { WORKER_CODE } from './worker-inline'` with the stub value ''.
// We need to replace that compiled stub with the real worker code.
let loaderSrc = fs.readFileSync(loaderPath, 'utf8');

// The compiled import either becomes:
//   import { WORKER_CODE } from './worker-inline.js';
// or tsc inlines the value. Check both cases.

if (loaderSrc.includes('./worker-inline')) {
  // Import is kept as a module reference — worker-inline.js will be loaded at runtime.
  // We already wrote the real worker-inline.js above, so nothing more to do.
  console.log('Loader imports worker-inline as module — no patch needed.');
} else {
  // tsc inlined the stub value. Find and replace it.
  // The stub exports: export const WORKER_CODE = '';
  // After inlining in the loader it becomes something like: const WORKER_CODE = "";
  const stubPatterns = [
    { find: 'const WORKER_CODE = ""', replace: `const WORKER_CODE = \`${escaped}\`` },
    { find: "const WORKER_CODE = ''", replace: `const WORKER_CODE = \`${escaped}\`` },
    { find: 'var WORKER_CODE = ""',   replace: `var WORKER_CODE = \`${escaped}\`` },
    { find: "var WORKER_CODE = ''",   replace: `var WORKER_CODE = \`${escaped}\`` },
  ];

  let patched = false;
  for (const { find, replace } of stubPatterns) {
    if (loaderSrc.includes(find)) {
      loaderSrc = loaderSrc.replace(find, replace);
      patched = true;
      console.log(`Patched loader: replaced stub "${find.slice(0, 40)}..."`);
      break;
    }
  }

  if (!patched) {
    console.warn('WARNING: Could not find WORKER_CODE stub in loader — worker may be empty!');
    console.warn('Searched in:', loaderPath);
  }

  fs.writeFileSync(loaderPath, loaderSrc, 'utf8');
}

console.log('Worker inline code generated:', inlinePath);
